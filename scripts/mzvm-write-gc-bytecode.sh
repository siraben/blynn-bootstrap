while [ "$i" -lt 20 ]; do
  printf '%b' '\001\001\000\000\000\002\001\002\000\000\000\017\000\000\000\000\002\000\000\000' >> "$out"
  i=$((i + 1))
done
printf '%b' '\001\117\000\000\000\016\001\000\000\000\001\000\000\000' >> "$out"
printf '%b' '\001\113\000\000\000\016\001\000\000\000\001\000\000\000' >> "$out"
printf '%b' '\001\012\000\000\000\016\001\000\000\000\001\000\000\000' >> "$out"
printf '%b' '\000' >> "$out"
